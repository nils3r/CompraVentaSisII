    public void muestra(String buscar) {
        try {
            DefaultTableModel modelo;
            Fcategoria fcat = new Fcategoria();
            modelo = fcat.mostrar(buscar);
            tblListado.setModel(modelo);
            tblTotalRegistros.setText("Nro de Registros: " + fcat.totalregistros);

        } catch (Exception e) {
        }

    }

    public void buscar(String buscar, String campo) {
        try {
            DefaultTableModel modelo;
            Fcategoria fcat = new Fcategoria();
            modelo = fcat.buscar(buscar, campo);
            tblListado.setModel(modelo);
            tblTotalRegistros.setText("Nro de Registros: " + fcat.totalregistros);

        } catch (Exception e) {
        }

    }

