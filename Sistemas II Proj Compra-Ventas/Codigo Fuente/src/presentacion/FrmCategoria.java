    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        Lcategoria dts = new Lcategoria();
        Fcategoria fcat = new Fcategoria();

        dts.setNombre(txtNombre.getText());
        dts.setDescripcion(txtDescripcion.getText());

        if (txtNombre.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un nombre, es obligatorio", "Sistema", JOptionPane.WARNING_MESSAGE);
            txtNombre.requestFocus();
            return;
        }
        String resp;
        if (accion.equals("guardar")) {

            //if(fcat.existe(txtNombre.getText())==1){
            if (fcat.insertar(dts)) {
                JOptionPane.showMessageDialog(null, "Registrado Correctamente");
                tabGeneral.setEnabledAt(0, true);
                tabGeneral.setEnabledAt(1, false);
                tabGeneral.setSelectedIndex(0);
                this.limpiar();
                muestra("");
            }
            /*}else{
            JOptionPane.showMessageDialog(null, "El nombre ya existe");
              
            }*/
        } else if (accion.equals("editar")) {
            dts.setId(Integer.parseInt(txtId.getText()));
            if (fcat.actualizar(dts)) {
                JOptionPane.showMessageDialog(null, "Actualizado Correctamente");
                tabGeneral.setEnabledAt(0, true);
                tabGeneral.setEnabledAt(1, false);
                tabGeneral.setSelectedIndex(0);
                muestra("");
                this.limpiar();
            }
        }
// TODO add your handling code here:
    }//GEN-LAST:event_btnGuardarActionPerformed

