    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (tblListado.getSelectedRowCount() == 1) {
            String id = String.valueOf(tblListado.getValueAt(tblListado.getSelectedRow(), 0));
            String nombre = String.valueOf(tblListado.getValueAt(tblListado.getSelectedRow(), 1));
            this.nombreS = String.valueOf(tblListado.getValueAt(tblListado.getSelectedRow(), 1));
            String descripcion = String.valueOf(tblListado.getValueAt(tblListado.getSelectedRow(), 2));

            txtId.setText(id);
            txtNombre.setText(nombre);
            txtDescripcion.setText(descripcion);

            tabGeneral.setEnabledAt(0, false);
            tabGeneral.setEnabledAt(1, true);
            tabGeneral.setSelectedIndex(1);

            this.accion = "editar";
            btnGuardar.setText("EDITAR");

        } else {

            JOptionPane.showMessageDialog(null, "Seleccione un registro para EDITAR");

        }

    }//GEN-LAST:event_jButton3ActionPerformed

